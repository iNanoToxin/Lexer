-- Division by Zero
print(5 / 0)

-- Floating-Point Precision
print(0.1 + 0.2)

-- Large Numbers
print(1e100 * 1e100)

-- Negative Numbers
print(-5 * -3)

-- Integer Overflow
print(2147483647 + 1)

-- Operator Precedence
print(2 + 3 * 4)
print((2 + 3) * 4)

-- Associativity
print(1 - 2 - 3)
print(1 - (2 - 3))

-- Parentheses
print(((1 + 2) * 3) / 4)

-- Mixed Operations
print(2 * 3 + 4 / 2)

-- Unary Operators
print(-5 + 3)
print(5 - 3)